			<ul>
				<li>Open a brokerage account. Most people use online brokers 
				such as Ameritrade or Etrade. They provide fair commission 
				prices and simple screens for trading. A brokerage account is a 
				like bank account that you fund that allows you to buy and sell 
				stocks. <br>
				&nbsp;</li>
				<li>Determine your objective. Do you want to day trade or 
				invest. Day trading or short term trading will require more time 
				and more focus on the stocks you want to trade. A longer term 
				strategy will not determine as much time but should involve more 
				research in the companies you plan to invest in. <br>
				&nbsp;</li>
				<li>Once you have your objective determined, buy the stocks you 
				like. Stop losses or an exit plan should always be in place when 
				buying any stock. An exit plan minimizes losses and helps profit 
				profits to the upside. <br>
				&nbsp;</li>
				<li>A good place to research companies is Yahoo Finance, Big 
				Charts, Pink Sheets.</li>
